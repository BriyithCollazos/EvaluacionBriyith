public class Main {
    public static void main(String args[]){
        //SUMA
        // declaracion
        int numero1;
        int numero2;
        //inicializacion
        numero1=5;
        numero2=8;
        //impremo la oparacion
        System.out.println(" la suma de "+numero1+" + "+numero2+" = "+(numero1+numero2));
        //RESTA
        // declaracion
        int numeroA;
        int numeroB;
        //inicializacion
        numeroA=120;
        numeroB=26;
        //impremo la oparacion
        System.out.println(" la resta de "+numeroA+" - "+numeroB+" = "+(numeroA-numeroB));
        // MULTIPLICACION
        // declaracion
        int numeroC;
        int numeroD;
        //inicializacion
        numeroC=10;
        numeroD=18;
        //impremo la oparacion
        System.out.println(" la multiplicacion de "+numeroC+" X "+numeroD+" = "+(numeroC*numeroD));
        //DIVISION
        // declaracion
        int numeroE;
        int numeroF;
        //inicializacion
        numeroE=25;
        numeroF=5;
        //impremo la oparacion
        System.out.println(" la division de "+numeroE+" / "+numeroF+" = "+(numeroE/numeroF));
        //MOD O RESTO
        // declaracion
        int numero3;
        int numero4;
        //inicializacion
        numero3=16;
        numero4=8;
        //impremo la oparacion
        System.out.println(" el RESIDUO de "+numero3+" % "+numero4+" = "+(numero3%numero4));

    }
}
